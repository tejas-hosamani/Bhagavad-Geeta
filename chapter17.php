verse-14
BG 17.14: Worship of the Supreme Lord, the Brahmins, the spiritual master, the wise, and the elders—when this is done with the observance of cleanliness, simplicity, celibacy, and non-violence—is declared as the austerity of the body.
verse-14

verse-15
अनुद्वेगकरं वाक्यं सत्यं प्रियहितं च यत् |
स्वाध्यायाभ्यसनं चैव वाङ्मयं तप उच्यते || 15||
verse-15
