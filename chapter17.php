verse-5
BG 17.5: Some people perform stern austerities that are not enjoined by the scriptures, but rather motivated by hypocrisy and egotism. Impelled by desire and attachment,
verse-5

verse-6
कर्षयन्त: शरीरस्थं भूतग्राममचेतस: |
मां चैवान्त:शरीरस्थं तान्विद्ध्यासुरनिश्चयान् || 6||
verse-6
karṣhayantaḥ śharīra-sthaṁ bhūta-grāmam achetasaḥ
māṁ chaivāntaḥ śharīra-sthaṁ tān viddhy āsura-niśhchayān
verse-6
